package Exception;

public class AlgorithmsExceptions extends Exception {
	private static final long serialVersionUID = 1L;

	public AlgorithmsExceptions() {
		super();
	}

	public AlgorithmsExceptions(String message) {
		super(message);
	}

	public AlgorithmsExceptions(String message, Throwable cause) {
		super(message, cause);
	}

	public AlgorithmsExceptions(Throwable cause) {
		super(cause);
	}

}
